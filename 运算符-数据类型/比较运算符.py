"比较运算符 ==等于 !=不等于 >大于  <小于   >=大于等于 <=小于等于"


salary1 = 10000
salary2 = 20000

print(salary1 == salary2) #比数值 - 等于
print(salary1 != salary2) #比数值 - 不等于

print(salary1 > salary2)
print(salary1 < salary2)
print(salary1 >= salary2)
print(salary1 <= salary2)
















